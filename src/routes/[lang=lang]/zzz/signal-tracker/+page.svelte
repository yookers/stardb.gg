	<h2 class="text-2xl font-bold">Gacha System and Character Rarities in Zenless Zone Zero</h2>
	<ol class="list-decimal">
		<li>
			<p><strong>Main Summonable Items</strong>:</p>
			<ul class="ml-4 list-disc">
				<li>
					<strong>Agents</strong>: Playable characters that expand your team&#39;s capabilities. Duplicate agents enhance
					existing characters by granting them new abilities.
				</li>
				<li>
					<strong>W-Engines</strong>: These are akin to weapons or Light Cones in Honkai: Star Rail, that equip agents with
					enhanced stats or special abilities. Notably, S-Rank W-Engines are not available from the gacha pulls as per CBT2.
				</li>
				<li>
					<strong>Bangboos</strong>: Mascot-like creatures that accompany agents in battle, providing team-wide boosts to ATK,
					HP, DEF, and Impact. Available in A-Rank and B-Rank variants through summons.
				</li>
			</ul>
		</li>
		<li>
			<p><strong>Banners (Channels)</strong>:</p>
			<ul class="ml-4 list-disc">
				<li>
					<strong>Exclusive Channel</strong>: This is for limited-time S-Rank characters, similar to the system in other
					Hoyoverse games. If an S-Rank pull does not yield the rate-up unit, the next S-Rank is guaranteed to be the featured
					character.
				</li>
				<li>
					<strong>Standard Channel</strong>: Features a fixed roster of S-Rank units without specific rate-ups. Offers a
					beginner-friendly discount on initial pulls, reducing the cost and guaranteeing an S-Rank agent within the first 50
					pulls.
				</li>
			</ul>
		</li>
		<li>
			<p><strong>ZZZ Pity System</strong>:</p>
			<ul class="ml-4 list-disc">
				<li>
					The gacha ensures a minimum frequency of high-rarity pulls. Players are guaranteed an A-Rank item every ten pulls and
					an S-Rank agent every 90 pulls, with increased odds starting at 75 and ending at 90.
				</li>
			</ul>
		</li>
	</ol>
